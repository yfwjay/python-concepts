# Variable name of type string
name = "James Kabuga" 
print(name)
# Variable name of type integer
age = 20
print(age)
# Variable name of type float
height = 5.9    
print(height)
# Variable name of type boolean 
is_student = True
print(is_student)   



# Type casting or what we call Type conversion

# Converting integer to float

age_float = float(age)
print(age_float)

# Converting float to integer

age_int = int(height)
print(age_int)


# Printing data_types

print(type(name))
print(type(age))
print(type(height)) 