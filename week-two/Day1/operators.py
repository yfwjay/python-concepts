# Expressions
# Any piece of code that returns a value.

num1 = 10
num2 = 2

# Operators

addition = num1 + num2
subtraction = num1 - num2
multiplication = num1 * num2
division = num1 / num2
modulus = num1 % num2
power = num1 ** num2
flow_division = num1 // num2

# Comparison Operators

print(num1 == num2)
print(num1 < num2)
print(num1 > num2)

# Logical operators

print(num1 < num2 and num1 == num2)
print(num1 < num2 or num1 > num2)
print(not(num1 > num2))

# Reassigning using assignment operators

# We increase the value of addition by 4
addition += 4  
print(addition)

# We decrease the value of addition by 4
addition -= 4

print(addition)


